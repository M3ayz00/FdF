#include "libft.h"

void	*ft_memmove(void *dest, void *src, size_t n)
{
	unsigned char		*s;
	unsigned char		*d;

	if (dest == src)
		return (dest);
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (d > s)
	{
		while (n-- > 0)
			d[n] = s[n];
	}
	else
		ft_memcpy(dest, src, n);
	return (dest);
}
